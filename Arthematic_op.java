public class Arthematic_op {
    public static void main(String[] args) {
        int a= 100;
        int b= 20;

        int add= a+b;
        System.out.println("Add number : " + add);

        int sub= a-b;
        System.out.println("Sub number : " + sub);

        int mul= a*b;
        System.out.println("Mull number : " + mul);

        int div= a+b;
        System.out.println("Div number : " + div);

        int module =a%b;
        System.out.println("module number : " + module);
    }
}
